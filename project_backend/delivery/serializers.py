from rest_framework import serializers

from orders.models import Order, ReturnItem
from .models import DepositRequest, ReturnRequest, ReturnRequestItem


class ReturnedProductSerializer(serializers.ModelSerializer):
    """A finalized returned product (orders.ReturnItem) for the agent's orders —
    includes returns generated by admin order edits, not just app submissions."""

    invoice_number = serializers.CharField(source="order.invoice_number", read_only=True)
    shop_name = serializers.SerializerMethodField()
    product_name = serializers.SerializerMethodField()
    line_total = serializers.SerializerMethodField()
    created_by_name = serializers.SerializerMethodField()
    source = serializers.SerializerMethodField()

    class Meta:
        model = ReturnItem
        fields = [
            "id", "order", "invoice_number", "shop_name",
            "product", "product_name", "quantity", "unit_price", "line_total",
            "reason", "created_by", "created_by_name", "source", "created_on",
        ]

    def get_shop_name(self, obj):
        return getattr(obj.order.user_id, "shop_name", None) if obj.order and obj.order.user_id else None

    def get_product_name(self, obj):
        return getattr(obj.product, "product_name", None) if obj.product else None

    def get_line_total(self, obj):
        return float((obj.unit_price or 0) * obj.quantity)

    def get_created_by_name(self, obj):
        return getattr(obj.created_by, "full_name", None) if obj.created_by else None

    def get_source(self, obj):
        # ReturnItems created by admin order-edits have no created_by; agent
        # (confirmed-request) returns record the delivery agent who triggered them.
        return "agent" if obj.created_by_id else "admin"


class ReturnRequestItemSerializer(serializers.ModelSerializer):
    product_name = serializers.SerializerMethodField()
    line_total = serializers.SerializerMethodField()

    class Meta:
        model = ReturnRequestItem
        fields = ["id", "product", "product_name", "quantity", "unit_price", "line_total", "reason"]

    def get_product_name(self, obj):
        return getattr(obj.product, "product_name", None) if obj.product else None

    def get_line_total(self, obj):
        return float(obj.quantity * (obj.unit_price or 0))


class ReturnRequestSerializer(serializers.ModelSerializer):
    items = ReturnRequestItemSerializer(many=True, read_only=True)
    delivery_man_name = serializers.SerializerMethodField()
    reviewed_by_name = serializers.SerializerMethodField()
    invoice_number = serializers.CharField(source="order.invoice_number", read_only=True)
    shop_name = serializers.SerializerMethodField()
    total_quantity = serializers.SerializerMethodField()
    total_value = serializers.SerializerMethodField()

    class Meta:
        model = ReturnRequest
        fields = [
            "id", "order", "invoice_number", "shop_name",
            "delivery_man", "delivery_man_name", "status", "note",
            "created_at", "reviewed_by", "reviewed_by_name", "reviewed_at", "review_note",
            "total_quantity", "total_value", "items",
        ]

    def get_delivery_man_name(self, obj):
        return getattr(obj.delivery_man, "full_name", None) if obj.delivery_man else None

    def get_reviewed_by_name(self, obj):
        return getattr(obj.reviewed_by, "full_name", None) if obj.reviewed_by else None

    def get_shop_name(self, obj):
        return getattr(obj.order.user_id, "shop_name", None) if obj.order and obj.order.user_id else None

    def get_total_quantity(self, obj):
        return sum(i.quantity for i in obj.items.all())

    def get_total_value(self, obj):
        return float(sum(i.quantity * (i.unit_price or 0) for i in obj.items.all()))


