class DeliveryOrderSerializer(serializers.ModelSerializer):
    """Compact order view for the delivery app's order list."""

    shop_name = serializers.SerializerMethodField()
    customer_name = serializers.SerializerMethodField()
    phone = serializers.SerializerMethodField()
    address = serializers.CharField(source="shipping_address", read_only=True)
    area = serializers.SerializerMethodField()
    invoice_amount = serializers.FloatField(source="total_amount", read_only=True)
    collected_amount = serializers.FloatField(read_only=True)
    due_amount = serializers.FloatField(read_only=True)
    payment_status = serializers.SerializerMethodField()
    delivery_status = serializers.CharField(source="order_status", read_only=True)
    assigned_to_name = serializers.SerializerMethodField()
    shop_latitude = serializers.SerializerMethodField()
    shop_longitude = serializers.SerializerMethodField()

    class Meta:
        model = Order
        fields = [
            "order_id",
            "invoice_number",
            "shop_name",
            "customer_name",
            "phone",
            "address",
            "area",
            "shop_latitude",
            "shop_longitude",
            "invoice_amount",
            "collected_amount",
            "due_amount",
            "payment_status",
            "delivery_status",
            "assigned_to",
            "assigned_to_name",
            "order_date",
        ]

    def get_assigned_to_name(self, obj):
        return getattr(obj.assigned_to, "full_name", None) if obj.assigned_to else None

    def get_shop_name(self, obj):
        return getattr(obj.user_id, "shop_name", None) if obj.user_id else None

    def get_customer_name(self, obj):
        return getattr(obj.user_id, "full_name", None) if obj.user_id else None

    def get_phone(self, obj):
        return getattr(obj.user_id, "phone", None) if obj.user_id else None

    def get_area(self, obj):
        user = obj.user_id
        return getattr(user.area, "area_name", None) if user and user.area else None

    def get_shop_latitude(self, obj):
        v = getattr(obj.user_id, "latitude", None) if obj.user_id else None
        return float(v) if v is not None else None

    def get_shop_longitude(self, obj):
        v = getattr(obj.user_id, "longitude", None) if obj.user_id else None
        return float(v) if v is not None else None

    def get_payment_status(self, obj):
        total = float(obj.total_amount or 0)
        collected = float(obj.collected_amount or 0)
        if collected <= 0:
            return "Due"
        if collected < total:
            return "Partial"
        return "Paid"


class DeliveryOrderItemSerializer(serializers.Serializer):
    """An order line as seen by the agent app (for delivery + return selection)."""
    product = serializers.IntegerField(source="product_id")
    product_name = serializers.SerializerMethodField()
    quantity = serializers.IntegerField()
    unit_price = serializers.FloatField()
    mrp = serializers.FloatField()
    discount_percent = serializers.FloatField()
    line_total = serializers.SerializerMethodField()

    def get_product_name(self, obj):
        return obj.product_name or (obj.product.product_name if obj.product else None)

    def get_line_total(self, obj):
        return float((obj.unit_price or 0) * obj.quantity)


class DeliveryOrderDetailSerializer(DeliveryOrderSerializer):
    """Compact order view + its line items (used by the agent order-detail endpoint)."""
    items = DeliveryOrderItemSerializer(many=True, read_only=True)

    class Meta(DeliveryOrderSerializer.Meta):
        fields = DeliveryOrderSerializer.Meta.fields + ["items"]
